/* public 是关键字，称为访问修饰符。它用于其他控制程序的其他部分对这段代码的访问级别。

 关键字class 表明java程序中的全部内容都包含在类中
 这里只需要将类作为一个加载程序逻辑的容器，程序逻辑定义了应用程序的行为。
*/
